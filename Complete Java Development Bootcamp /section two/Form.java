public class Form {
    public static void main(String[] args) {
        // single quotes are for char variables
        char gender = 'F';
        String name = "jenna";
        System.out.println("Gender : " + gender);
        System.out.println("Name : " + name);
        // do not always use String when storing a char because of memory and 
        // performance. 
    }
}
